            <h4 class="h6 pt-2">Objetivos do Centro Operacional: </h4>
            
            <ul>
                <li>Localizar e instalar as réplicas (logomarcas) dos terminais portuários no cenário das operações: margem direita e margem esquerda – porto organizado e terminais de uso privativo;</li>
                <li>Localizar e instalar as geo referências (limites territoriais), pontos turísticos e outras locações municipais e portuárias;</li>
                <li>Instalar os equipamentos de carga e descarga: shiploaders e portainers;</li>
                <li>A partir das informações da Praticagem e da Autoridade Portuária (e demais fontes selecionadas), “atracar” os navios em seus respectivos pontos de atracação;</li>
                <li>Distribuir os navios em espera na Barra;</li>
                <li>Distribuir as embarcações de apoio: rebocadores, dragas, balsas etc.;</li>
                <li>Comparar as informações da Praticagem e Autoridade Portuária com as informações do satélite: “AIS” – site Maritime Traffic;</li>
                <li>Acionar os comandos de sinalização dos acessos rodoviários nas perimetrais a partir das informações sobre congestionamentos, acidentes, acessos restritos etc.;</li>
                <li>Acionar os comandos da sinalização dos ramais ferroviários;</li>
                <li>Sinalizar as informações sobre as marés e calado;</li>
                <li>Sinalizar as condições climáticas (Estações Meteorológicas);</li>
                <li>Sinalizar os obstáculos constantes da carta náutica;</li>
                <li>Sinalizar o sistema de boias do canal do porto;</li>
                <li>Simular acidentes e situações de emergência no canal do porto;</li>
                <li>Simular manobras – mudanças de atracação entre berços;</li>
                <li>Simular manobras – giro, bombordo e boreste;</li>
                <li>Simular manobras dos rebocadores – atracações e desatracações;</li>
                <li>Simular interrupções por dragagem, construções, reformas etc.;</li>
                <li>Simular a disponibilização de equipamentos nos berços;</li>
                <li>Simular gargalos da logística portuária e dos acessos;</li>
                <li>Simular operações com embarques das “safras”;</li>
                <li>Simular operações com cargas de projeto, cargas perigosas e cargas vivas;</li>
                <li>Simular situações de interesse do PAM – acidentes em terra e mar;</li>
                <li>Simular o embarque simultâneo de cruzeiros (Até 8 navios);</li>
                <li>Simular a produtividade por berços e equipamentos (containers e granéis).</li>
